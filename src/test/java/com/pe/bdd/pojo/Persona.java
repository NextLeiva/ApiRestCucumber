package com.pe.bdd.pojo;

public class Persona {
    public int id;
    public String nombre;
    public String email;
    public String phone;

    public void setId(int id) {
        this.id = id;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
